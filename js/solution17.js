/*Convert a String to a Number!

We need a function that can transform a string into a number. 
Note: All inputs will be strings, and every string is a perfectly valid representation of an integral number.

*/

// Number() converts a string to the number type. If the value can't be converted, it returns NaN
//parseInt() can also be used to convert the string to the number


const stringToNumber = str => Number(str)


